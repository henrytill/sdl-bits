#include <stdlib.h>

#include "bmp.h"

enum {
	SUCCESS = 0,
	FAILURE = 1,
};

enum {
	WIDTH = 4,
	HEIGHT = 2,
};

static const struct bmp_Pixel32 BUF[] = {
	{0xFF, 0x00, 0x00, 0x7F},
	{0x00, 0xFF, 0x00, 0x7F},
	{0x00, 0x00, 0xFF, 0x7F},
	{0xFF, 0xFF, 0xFF, 0x7F},
	{0xFF, 0x00, 0x00, 0xFF},
	{0x00, 0xFF, 0x00, 0xFF},
	{0x00, 0x00, 0xFF, 0xFF},
	{0xFF, 0xFF, 0xFF, 0xFF},
};

static const char *const BMPFILE = "./test.bmp";

int
main(int argc, char *argv[])
{
	int rc = FAILURE;

	(void)argc;
	(void)argv;

	rc = bmp_v4write(BUF, WIDTH, HEIGHT, BMPFILE);
	if (rc != SUCCESS) {
		return EXIT_FAILURE;
	}
	return EXIT_SUCCESS;
}
