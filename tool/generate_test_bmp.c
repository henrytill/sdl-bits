#include "bmp.h"

enum {
	WIDTH = 4,
	HEIGHT = 2,
};

static const struct bmp_Pixel32 BUF[] = {
	{0xFF, 0x00, 0x00, 0x7F},
	{0x00, 0xFF, 0x00, 0x7F},
	{0x00, 0x00, 0xFF, 0x7F},
	{0xFF, 0xFF, 0xFF, 0x7F},
	{0xFF, 0x00, 0x00, 0xFF},
	{0x00, 0xFF, 0x00, 0xFF},
	{0x00, 0x00, 0xFF, 0xFF},
	{0xFF, 0xFF, 0xFF, 0xFF},
};

static const char *const BMPFILE = "./test.bmp";

int
main(int argc, char *argv[])
{
	(void)argc;
	(void)argv;

	return bmp_v4write(BUF, WIDTH, HEIGHT, BMPFILE);
}
